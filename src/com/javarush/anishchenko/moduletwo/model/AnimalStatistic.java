package com.javarush.anishchenko.moduletwo.model;

public class AnimalStatistic {

    private int died;

    private int born;

    private int eaten;

    public int getDied() {
        return died;
    }

    public void setDied(int died) {
        this.died = died;
    }

    public int getBorn() {
        return born;
    }

    public void setBorn(int born) {
        this.born = born;
    }

    public int getEaten() {
        return eaten;
    }

    public void setEaten(int eaten) {
        this.eaten = eaten;
    }
}
